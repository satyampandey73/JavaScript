// data type conversion

let score="45"

console.log(typeof score)
console.log(score)

let num=Number(score)

console.log(typeof num)
console.log(num)

let str=String(score)

console.log(typeof str)
console.log(str)

let bol=Boolean(str)

console.log(typeof bol)
console.log(bol)

// "33" -> 33
// "22s" -> NaN

